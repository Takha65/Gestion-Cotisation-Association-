    public void keyTyped(KeyEvent k)  
     {  
         // cette methode permet le refus d'entrer de lettre sur un champ !!
      if (! Character.isDigit(k.getKeyChar()))  
      {  
       k.consume();
      }    
    }
    public void keyTyped2(KeyEvent e) {
      // cette methode permet le refus d'entrer des caracteres speciales  !!
        char c = e.getKeyChar();
        if(!(Character.isAlphabetic(c) || (c!=KeyEvent.VK_BACK_SPACE) || c==KeyEvent.VK_DELETE ))
        {
            e.consume();  // ignorer l'événement
        }
    }
    
    public void remplirMembreInfos(ResultSet rs) throws SQLException{
        if(rs.next()){
            avertissement_matricule.setText("");
            JLprenom.setText(rs.getString("prenom"));
            JLnom.setText(rs.getString("nom"));
            JLadresse.setText(rs.getString("adresse"));
            JLMatricule.setText(rs.getString("matricule"));
            JLposte.setText(rs.getString("poste"));
            JLnumero.setText(rs.getString("numero"));
            jComboBox_etat.setSelectedItem(rs.getString("etat"));
            id_membre_update=Integer.parseInt(rs.getString("id"));
            Btn_Soumettre.setText("Modifier");
            jLabel22.setText("Modifier Membre");
            System.out.println(Btn_Soumettre.getActionCommand());

        }
        else{
            clearMemberForm();
            avertissement_matricule.setText("Erreur cette matricule n'existe pas!");  
        }


    }
   
