    private void Btn_SoumettreActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Btn_SoumettreActionPerformed
        // TODO add your handling code here:
      String nom = JLnom.getText();
      String prenom=JLprenom.getText();
      String poste = JLposte.getText();
      
      String adresse=JLadresse.getText();
      String matricule=JLMatricule.getText();
      String etat= (String) jComboBox_etat.getSelectedItem();
        if(nom.equals("")|| prenom.equals("")||poste.equals("")||JLnumero.getText().equals("")||adresse.equals("")||matricule.equals("") )
        {
         JOptionPane.showMessageDialog(this,"VEUILLER REMPLIR TOUS LES CHAMPS ","ERREUR!!",JOptionPane.INFORMATION_MESSAGE,null);
         return ;

          
        }
        else if(Btn_Soumettre.getText().equals("Soumettre"))
        {
           int numero = Integer.parseInt(JLnumero.getText());
          Membre M= new Membre(prenom,nom,poste,matricule,adresse,numero,etat);  
          try{
            
              int res= new Database_Connect().SaveMembreOnDB(M);
            
              if(res==0){
                  cleanLabel();
                 JOptionPane.showMessageDialog(this,"Un nouveau membre a été bien ajouté ","Notification !!",JOptionPane.INFORMATION_MESSAGE,null);
                 return ;   
              }
              else{
                  JOptionPane.showMessageDialog(this,"La matricule existe déjà !!! Veuillez en prendre une autre ","ERREUR!!",JOptionPane.INFORMATION_MESSAGE,null);
                 return ;
              }
            
          }
          catch(SQLException sl){
              //System.out.println("Hakaza Falll");
             
          }

            
        }
        else if(Btn_Soumettre.getText().equals("Modifier")){
            int numero = Integer.parseInt(JLnumero.getText());
            Membre M= new Membre(prenom,nom,poste,matricule,adresse,numero,etat); 
          try {
              new Database_Connect().UpdateMembreOnDB(M,id_membre_update);
          } catch (SQLException ex) {
              Logger.getLogger(Nouveau_Membre.class.getName()).log(Level.SEVERE, null, ex);
          }
           
            Btn_Soumettre.setText("Soumettre");
          
            Jmatricule.setVisible(false);
            jLabel_membre.setVisible(false);
            jToggleButton2.setVisible(false);
            jLabel22.setText("Nouveau Membre");
            JLMatricule.setText("");
            JLadresse.setText("");JLnom.setText("");
            JLnumero.setText("");JLposte.setText("");
            JLprenom.setText("");
            Jmatricule.setText("");
            id_membre_update=0;
            JOptionPane.showMessageDialog(this,"Les informations du membre ont été bien modifiées ","Notification !!",JOptionPane.INFORMATION_MESSAGE,null);
            return ;
        }

    }//GEN-LAST:event_Btn_SoumettreActionPerformed

